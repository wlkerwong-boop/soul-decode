/**
 * 按用户级别获取可用模板
 */
export function getTemplatesByLevel(level: 'free' | 'paid' | 'vip'): PromptTemplate[] {
  const levels = { free: ['free'], paid: ['free', 'paid'], vip: ['free', 'paid', 'vip'] };
  const allowed = levels[level] || ['free'];
  return PROMPT_TEMPLATES.filter(t => allowed.includes(t.userLevel));
}

/**
 * 按类别获取模板
 */
export function getTemplatesByCategory(category: TemplateCategory): PromptTemplate[] {
  return PROMPT_TEMPLATES.filter(t => t.category === category);
}

/**
 * 获取推荐模板（基于用户行为和心理阶段）
 */
export function getRecommendedTemplates(hasReport: boolean, hasPaid: boolean): PromptTemplate[] {
  if (!hasReport) return [];
  if (!hasPaid) return getTemplatesByLevel('free');
  // 付费用户推荐：行动计划 + 自我深化 + 决策支持
  const priority: TemplateCategory[] = ['action-plan', 'self-deepening', 'decision-support'];
  const result: PromptTemplate[] = [];
  for (const cat of priority) {
    result.push(...getTemplatesByCategory(cat).filter(t => t.userLevel !== 'vip'));
  }
  return result.slice(0, 5);
}
