/**
 * 构建完整的用户信息上下文
 */
export function buildUserContext(params: {
  year: number;
  month: number;
  day: number;
  hour?: number;
  location?: string;
  gender?: string;
  timezone?: string;
  lifePathNumber: number;
  isMasterNumber: boolean;
  timePeriod: string | null;
  cityTags: string[];
  cityDescription: string | null;
}): string {
  const lines = [
    `【用户出生信息】`,
    `出生日期：${params.year}年${params.month}月${params.day}日`,
    params.hour !== undefined ? `出生时辰：${params.hour}时${params.timePeriod ? `（${params.timePeriod}）` : ''}` : '',
    params.gender ? `性别：${params.gender}` : '',
    params.timezone ? `出生时区：${params.timezone}` : '',
    `出生地点：${params.location || '未提供'}`,
    '',
    `【计算数据】`,
    `生命路径数字：${params.lifePathNumber}${params.isMasterNumber ? '（主数）' : ''}`,
    params.timePeriod ? `时辰能量：${params.timePeriod}` : '',
    '',
    `【地域背景】`,
    params.cityDescription ? `地域描述：${params.cityDescription}` : '',
    params.cityTags.length > 0 ? `地域标签：${params.cityTags.join('、')}` : '',
    '',
    `请根据以上信息，为用户生成一份深度的个人使命与天赋解码报告。`,
  ];

  return lines.filter(l => l).join('\n');
}

